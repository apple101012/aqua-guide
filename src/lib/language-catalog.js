export function getLanguageExperience(language, region) {
  const config = languageCatalog[language] || languageCatalog.en;
  const place = region?.name || "this place";
  const fill = (template) => String(template || "").replaceAll(/\{(\w+)\}/g, (_, key) => ({ place })[key] ?? "");
  return {
    label: config.label,
    placeholder: config.placeholder,
    intro: fill(config.intro),
    sampleQuestion: fill(config.sampleQuestion),
    sampleAnswer: fill(config.sampleAnswer),
    suggestions: (config.suggestionTemplates || languageCatalog.en.suggestionTemplates).map(fill)
  };
}
